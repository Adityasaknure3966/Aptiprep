        <h2>Question 1: Arithmetic Sequence Problem</h2>
            
            <p><strong>Question:</strong> Find the missing number in the sequence: <span class="highlight">3, 6, 11, 18, 27, ?, 51</span></p>
            
            <p><strong>Solution:</strong> The solution of the series is as follows:</p>
            
            <div class="solution-steps">
                <p>3 + 3 = 6</p>
                <p>6 + 5 = 11</p>
                <p>11 + 7 = 18</p>
                <p>18 + 9 = 27</p>
                <p>27 + 11 = <span class="highlight">38</span></p>
                <p>38 + 13 = 51</p>
            </div>
            <p>Hence, the correct answer is: <span class="highlight">38</span>.</p>
            <h2>Question 2: Arithmetic Sequence Problem</h2>
    
            <p><strong>Question:</strong> Find the missing number in the sequence: <span class="highlight">5, 11, 24.2, 53.24, ?, 257.6816</span></p>
            
            <p><strong>Solution:</strong> The solution of the series is as follows:</p>
            
            <div class="solution-steps">
                <p>5 × 2.2 = 11</p>
                <p>11 × 2.2 = 24.2</p>
                <p>24.2 × 2.2 = 53.24</p>
                <p>53.24 × 2.2 = <span class="highlight">117.128</span></p>
                <p>117.128 × 2.2 = 257.6816</p>
            </div>
            
            <p>Hence, the correct answer is: <span class="highlight">117.128</span>.</p>







