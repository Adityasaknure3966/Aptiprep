        <h3>Key Formulas:</h3>
        <ul>
            <li>Syllogism: <br>
            Premise 1: All A are B.<br>
            Premise 2: All B are C.<br>
            <strong>Conclusion:</strong> Therefore, all A are C.</li>
            <li>Blood Relations: <br>
            - Father's son = Brother<br>
            - Mother's sister = Aunt<br>
            - Son's wife = Daughter-in-law<br></li>
            <li> Number Series: <br>
            Arithmetic Series: \(a_n = a_1 + (n-1)d\)<br>
            Geometric Series: \(a_n = a_1 \times r^{n-1}\)<br></li>
            <li> Seating Arrangement: <br>
            - Linear: A sits to the left of B.<br>
            - Circular: A sits between B and C.</li>
            <li> Statement and Assumptions: <br>
            Statement: "All students are hardworking."<br>
            Assumption: Students generally work hard.</li>
            <li>Coding-Decoding: <br>
            Code: CAT -> ECV<br>
            Rule: Shift each letter by 2 positions forward.</li>
            <li>Cause and Effect: <br>
            - Cause: The company increased salaries.<br>
            - Effect: Employee satisfaction improved.</li>

