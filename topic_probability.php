    <div class="formula-container">
        <h3>Key Formulas:</h3>
        <ul>
            <li>Probability: <br>
            P(A) = Number of favorable outcomes / Total number of outcomes</sup></li>
            <li> Complementary Probability: <br>
            P(A') = 1 - P(A)</li>
            <li>Conditional Probability: <br>
            P(A | B) = P(A ∩ B) / P(B)</li>
            <li>Addition Rule of Probability (Mutually Exclusive): <br>
            P(A ∪ B) = P(A) + P(B)</li>
            <li>Multiplication Rule of Probability: <br>
            P(A ∩ B) = P(A) × P(B)</li>
            <li>Bayes' Theorem: <br>
            P(A | B) = [P(B | A) × P(A)] / P(B)</li>
            <li>Expected Value (Mean): <br>
            E(X) = Σ [x_i × p_i]</li>
           

        </ul>
