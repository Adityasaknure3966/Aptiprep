    <h2>Sentence Correction</h2>
    <p><strong>Question 1:</strong> Identify the part of the sentence that has an error.</p>
    <p>"The company’s profits have been steadily rising for the past few years, and it has seen an increase in both revenue as well as market share."</p>
    
    <ul>
        <li>(a) The company's profits have been steadily rising</li>
        <li>(b) For the past few years</li>
        <li>(c) It has seen an increase in both revenue as well as market share</li>
        <li>(d) No error</li>
    </ul>
    
    <p><strong>Answer:</strong> (c) It has seen an increase in both revenue as well as market share</p>
    
    <p><strong>Explanation:</strong> The phrase "both revenue as well as market share" is incorrect. The correct phrase should be "both revenue and market share."</p>
