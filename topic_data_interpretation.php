        <h3>Key Formulas:</h3>
        <ul>
            <li> Percentage = <strong>(Value / Total Value) &times; 100</strong></sup></li>
            <li> Percentage Change = <strong>((New Value - Old Value) / Old Value) &times; 100</strong></li>
            <li>Ratio = <strong>Part 1 / Part 2</strong></li>
            <li>Average = <strong>Sum of values / Number of values</strong></li>
            <li> Profit = <strong>Selling Price - Cost Price</strong><br>
            Loss = <strong>Cost Price - Selling Price</strong><br>
            Profit Percentage = <strong>(Profit / Cost Price) &times; 100</strong><br>
            Loss Percentage = <strong>(Loss / Cost Price) &times; 100 </strong></li>
            <li>Compound Interest = <strong>A = P(1 + r/100)<sup>t</sup></strong></li>
            <li>Simple Interest = <strong>(P &times; r &times; t) / 100</strong></li>
            <li>Growth Rate = <strong>((Final Value - Initial Value) / Initial Value) &times; 100</strong></li>
            <li>Population Growth Rate = <strong>((Population at End - Population at Start) / Population at Start) &times; 100</strong></li>
            <li>Weighted Average = <strong>&Sigma;(Weight &times; Value) / &Sigma;Weights</strong></li>
            <li>Index Number = <strong>(Current Year Value / Base Year Value) &times; 100</strong></li>
